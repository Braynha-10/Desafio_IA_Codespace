# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!
# Pede ao usuário para inserir duas strings
string1 = input("Digite a primeira string: ")
string2 = input("Digite a segunda string: ")

# Concatena as strings usando o operador '+'
string_concatenada = string1 +" "+ string2

# Imprime a string concatenada
print("A string concatenada é:", string_concatenada)

# Neste código:
# Usamos input() para pedir ao usuário que digite duas strings.
# Armazenamos as strings inseridas nas variáveis string1 e string2.
# Usamos o operador + para concatenar as duas strings e armazenar o resultado na variável string_concatenada.
# Finalmente, imprimimos a string concatenada usando print().